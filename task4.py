print("Введите первое число: ")
num1 = int(input())
print("Введите второе число: ")
num2 = int(input())
if num1 > num2:
    print("Число ", num1,  "больше, чем число", num2)
else:
    print("Число ", num2, "больше, чем число", num1)

if num1 < num2:
    print("Число ", num1, "меньше, чем число", num2)
else:
    print("Число ", num2, "меньше, чем число", num1)
